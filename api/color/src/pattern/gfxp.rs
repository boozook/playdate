//! A collection of patterns based on [the GFXP library](https://github.com/ivansergeev/gfxp).
//!
//! ### Usage
//!
//! ```rust
//! extern crate playdate_color as color;
//! use color::{Color, Pattern};
//! use color::pattern::gfxp::LIGHT_GRAY;
//! use color::pattern::opaque;
//!
//! // Make pattern with opaque mask:
//! let pat: Pattern = opaque(LIGHT_GRAY);
//!
//! // Now make a color:
//! // CAUTION: this is on-stack reference. In real life put pattern into heap or static.
//! let color: Color = (&pat).into();
//!
//!
//! // Also any part of a pattern can be inverted:
//! use color::pattern::{invert_body, invert_mask};
//! let inverted = invert_body(pat);
//! let inverted = invert_mask(pat);
//! // As well entier pattern:
//! use color::pattern::invert;
//! let inverted = invert(pat);
//! ```
